        $rowsFromPost[$key] = about_page_rows_from_post(
            is_array($post[$columnKey] ?? null) ? $post[$columnKey] : [],
            $fields,
            $default[$key]
        );
    }

    return [
        'kicker_suffix' => homepage_text($post['kicker_suffix'] ?? $default['kicker_suffix']),
        'heading_line1' => homepage_text($post['heading_line1'] ?? $default['heading_line1']),
        'heading_line2' => homepage_text($post['heading_line2'] ?? $default['heading_line2']),
