    ];
}

function about_page_rows_from_post(array $columns, array $fields, array $default): array
{
    $count = 0;

    foreach ($fields as $field) {
        if (isset($columns[$field]) && is_array($columns[$field])) {
            $count = max($count, count($columns[$field]));
        }
    }

    if ($count === 0) {
        return $default;
    }

    $rows = [];

    for ($i = 0; $i < $count; $i++) {
        $row = [];
        $hasContent = false;

        foreach ($fields as $field) {
            $value = $field === 'text' || $field === 'description'
                ? homepage_textarea($columns[$field][$i] ?? '')
                : homepage_text($columns[$field][$i] ?? '');
            $row[$field] = $value;

            if ($value !== '') {
                $hasContent = true;
            }
        }

        if ($hasContent) {
            $rows[] = $row;
        }
    }

    return $rows === [] ? $default : $rows;
}

function about_page_save_content(array $content, ?int $adminId = null): void
{
    $json = json_encode($content, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE | JSON_THROW_ON_ERROR);

    $statement = db()->prepare(
        'INSERT INTO site_contents (content_key, content_json, updated_by)
         VALUES (:content_key, :content_json, :updated_by)
         ON DUPLICATE KEY UPDATE
             content_json = VALUES(content_json),
             updated_by = VALUES(updated_by),
             updated_at = CURRENT_TIMESTAMP'
    );

    $statement->execute([
        'content_key' => 'about_page',
        'content_json' => $json,
        'updated_by' => $adminId,
    ]);
}
