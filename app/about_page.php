function about_page_content(): array
{
    try {
        $statement = db()->prepare('SELECT content_json FROM site_contents WHERE content_key = :key LIMIT 1');
        $statement->execute(['key' => 'about_page']);
        $row = $statement->fetch();

        if ($row) {
            $stored = json_decode((string) $row['content_json'], true, 512, JSON_THROW_ON_ERROR);

            if (is_array($stored)) {
                return about_page_normalize($stored);
            }
        }

        $bootstrapped = about_page_bootstrap_from_homepage();
        about_page_save_content($bootstrapped, null);

        return $bootstrapped;
    } catch (Throwable) {
        return about_page_default_content();
    }
}

function about_page_bootstrap_from_homepage(): array
{
    $default = about_page_default_content();

    try {
        $homepage = homepage_content();
    } catch (Throwable) {
        return $default;
    }

    $hero = is_array($homepage['hero'] ?? null) ? $homepage['hero'] : [];
    $profile = is_array($homepage['profile'] ?? null) ? $homepage['profile'] : [];
    $focus = is_array($homepage['focus'] ?? null) ? $homepage['focus'] : [];
    $expertise = is_array($homepage['expertise'] ?? null) ? $homepage['expertise'] : [];
    $research = is_array($homepage['research'] ?? null) ? $homepage['research'] : [];
    $schedule = is_array($homepage['schedule'] ?? null) ? $homepage['schedule'] : [];
    $quotes = is_array($homepage['quotes'] ?? null) ? array_values($homepage['quotes']) : [];

    $seed = $default;
    $seed['kicker_suffix'] = (string) ($hero['kicker_suffix'] ?? $default['kicker_suffix']);
    $seed['hero_role'] = (string) ($hero['role'] ?? $default['hero_role']);
    $seed['hero_lede'] = (string) ($hero['lede'] ?? $default['hero_lede']);
    $seed['hero_stats'] = (is_array($hero['stats'] ?? null) && $hero['stats'] !== []) ? array_values($hero['stats']) : $default['hero_stats'];
    $seed['portrait_src'] = (string) ($profile['image']['src'] ?? $default['portrait_src']);
    $seed['portrait_alt'] = (string) ($profile['image']['alt'] ?? $default['portrait_alt']);
    $seed['recommendations'] = (is_array($homepage['recommendations'] ?? null) && $homepage['recommendations'] !== [])
        ? array_values($homepage['recommendations'])
        : $default['recommendations'];
    $seed['recommendations_note'] = (string) ($homepage['recommendations_note'] ?? $default['recommendations_note']);
    $seed['profile_heading'] = (string) ($profile['heading'] ?? $default['profile_heading']);
    $seed['profile_lead_html'] = (string) ($profile['lead_html'] ?? $default['profile_lead_html']);
    $seed['profile_paragraphs_html'] = (is_array($profile['paragraphs_html'] ?? null) && $profile['paragraphs_html'] !== [])
        ? array_values($profile['paragraphs_html'])
        : $default['profile_paragraphs_html'];
    $seed['focus_items'] = (is_array($focus['items'] ?? null) && $focus['items'] !== []) ? array_values($focus['items']) : $default['focus_items'];
    $seed['principles_quote'] = ($quotes[0] ?? null) !== null ? (string) $quotes[0] : $default['principles_quote'];
    $seed['expertise_heading'] = (string) ($expertise['heading'] ?? $default['expertise_heading']);
    $seed['expertise_items'] = (is_array($expertise['items'] ?? null) && $expertise['items'] !== []) ? array_values($expertise['items']) : $default['expertise_items'];
    $seed['research_heading'] = (string) ($research['heading'] ?? $default['research_heading']);
    $seed['research_items'] = (is_array($research['items'] ?? null) && $research['items'] !== []) ? array_values($research['items']) : $default['research_items'];
    $seed['research_media'] = (is_array($research['media'] ?? null) && $research['media'] !== []) ? array_values($research['media']) : $default['research_media'];
    $seed['research_note'] = (string) ($research['note'] ?? $default['research_note']);
    $seed['schedule_eyebrow'] = (string) ($schedule['eyebrow'] ?? $default['schedule_eyebrow']);
    $seed['schedule_heading'] = (string) ($schedule['heading'] ?? $default['schedule_heading']);
    $seed['schedule_description'] = (string) ($schedule['description'] ?? $default['schedule_description']);

    return about_page_normalize($seed);
}

function about_page_normalize(array $raw): array
{
    $default = about_page_default_content();
    $merged = array_merge($default, $raw);

    foreach (about_page_row_fields() as $key => $fields) {
        $merged[$key] = about_page_normalize_rows($raw[$key] ?? null, $default[$key], $fields);
    }

    $merged['profile_lead_html'] = homepage_rich_text($raw['profile_lead_html'] ?? $default['profile_lead_html']);
    $merged['profile_paragraphs_html'] = (is_array($raw['profile_paragraphs_html'] ?? null) && $raw['profile_paragraphs_html'] !== [])
        ? array_values(array_filter(array_map('homepage_rich_text', $raw['profile_paragraphs_html']), static fn (string $p): bool => $p !== ''))
        : $default['profile_paragraphs_html'];

    return $merged;
}

function about_page_normalize_rows(mixed $rows, array $default, array $fields): array
{
    if (! is_array($rows) || $rows === []) {
        return $default;
    }

    $normalized = [];

    foreach ($rows as $row) {
        if (! is_array($row)) {
            continue;
        }

        $entry = [];

        foreach ($fields as $field) {
            $entry[$field] = $field === 'text' || $field === 'description'
                ? homepage_textarea($row[$field] ?? '')
                : homepage_text($row[$field] ?? '');
        }

        $normalized[] = $entry;
    }

    return $normalized === [] ? $default : $normalized;
}

